using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
using Microsoft.EntityFrameworkCore;
using NetCoreTemplate1.Data;
using NetCoreTemplate1.Models;

namespace NetCoreTemplate1
{
    public class DetailsModel : PageModel
    {
        private readonly NetCoreTemplate1.Data.NetCoreTemplate1Context _context;

        public DetailsModel(NetCoreTemplate1.Data.NetCoreTemplate1Context context)
        {
            _context = context;
        }

        public Movie Movie { get; set; }

        public async Task<IActionResult> OnGetAsync(int? id)
        {
            if (id == null)
            {
                return NotFound();
            }

            Movie = await _context.Movie.FirstOrDefaultAsync(m => m.ID == id);

            if (Movie == null)
            {
                return NotFound();
            }
            return Page();
        }
    }
}
